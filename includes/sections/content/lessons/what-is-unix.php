		<p>
		<h3>What is UNIX</h3>
		UNIX is an operating system, a collection of software that manages computer hardware. It was developed in the 1960s at Bell Labs. It is a stable, multi-user, multi-tasking system for servers, desktops and laptops.
		</p>
		<p>
		Like Windows, UNIX has a graphical user interface (GUI) which allows easy use of the software environment, however certain components of UNIX does not have a GUI and will require knowledge of the operating system.
		</p>
		<p>
		<h3>The UNIX System</h3>
		UNIX is comprised of three parts: The kernel, the shell, and the programs
		<ul>
		<li>The Kernal: The kernal of UNIX is the hub of the operating system. It allocates time and memory to programs and handles the filestore and communications in response to system calls. The kernal also provides drivers for controlling software.</li>
		<li>The Shell: The shell is an interactive program that provides an interface between the user and the kernal. The user enters commands into the shell, then the shell intrepets the commands and passes them to the kernal fo execution.</li>
		</ul>
		</p>
		<p>
		<h3>Types of UNIX</h3>
		While there are many different versions of UNIX, they all share similarities. There are four operating systems that are certified by The Open Group (holder of the UNIX trademark) as UNIX: Sun Microsystems's Solaris, Apple's OS X, HP's HP-UX, and IBM AIX. Linux is considered a "Unix Like" system, as it behaves like a UNIX system, while not necessiarly confirming to the UNIX standards.
		</p>
